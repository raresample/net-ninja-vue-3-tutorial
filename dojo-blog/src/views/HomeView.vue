<template>
  <div class="home">
    home
    <p>My name is {{ name }}.  And my age is {{ age }}.</p>
    <button @click="handleClick">click me</button>
    <button @click="age++">Add 1 year</button>
    <input type="text" v-model="name">
  </div>
</template>

<script>
import { ref } from '@vue/reactivity'
// @ is an alias to /src

export default {
  name: 'HomeView',
  setup() {
    // const p = ref(null)

    const name = ref('mario')
    const age = ref(30)

    const handleClick = () => {
      name.value = 'luigi'
      age.value = 35
    }

    return { name, age, handleClick }
  }
}
</script>
